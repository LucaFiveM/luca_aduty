function notify(text)
    TriggerEvent('codem-notification', text, 5000, "info", options)
end

function announce(text)
    TriggerClientEvent('codem-notification', -1, text, 10000, "ann", options)
end
 
function DrawNameTags(x, y, z, text, r, g, b, scale)
    SetDrawOrigin(x, y, z, 0)
    SetTextFont(0)
    SetTextProportional(0)
    SetTextScale(0, scale or 0.2)
    SetTextColour(r, g, b, 255)
    SetTextDropshadow(0, 0, 0, 0, 255)
    SetTextEdge(2, 0, 0, 0, 150)
    SetTextDropShadow()
    SetTextOutline()
    SetTextEntry("STRING")
    SetTextCentre(1)
    AddTextComponentString(text)
    DrawText(0, 0)
    ClearDrawOrigin()
end

function setAdutyOutfit()
    TriggerEvent('skinchanger:getSkin', function(skin)
    ESX.TriggerServerCallback("luca_adminduty:getPlayerGroup", function(playerGroup)
        local playerSex = skin.sex
        if playerSex == 0 then
            if playerGroup == "superadmin" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.superadmin.male)
                elseif playerGroup == "admin" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.admin.male)
                elseif playerGroup == "mod" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.mod.male)
                elseif playerGroup == "support" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.support.male)					
                end
            else
                if playerGroup == "superadmin" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.superadmin.female)
                elseif playerGroup == "admin" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.admin.female)
                elseif playerGroup == "mod" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.mod.female)
                elseif playerGroup == "support" then
                    TriggerEvent("skinchanger:loadClothes", skin, Luca.Outfits.support.female)					
                end
            end
        end)
    end)
end